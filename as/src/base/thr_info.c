static void
cmd_help(const char* name, const char* params, cf_dyn_buf* db)
{
	const char* cmd_name = strchr(params, ARG_SEP);

	if (cmd_name != NULL) {
		cmd_name++;
		const as_info_cmd* cmd = find_cmd(cmd_name, strlen(cmd_name));

		if (cmd == NULL) {
			info_respond_error(db, AS_ERR_PARAMETER, "could not find command '%s'",
					cmd_name);
			return;
		}

		info_append_string(db, "command", cmd_name);
		info_append_bool(db, "enterprise-only", cmd->ee_only);
		info_append_bool(db, "client-only", cmd->client_only);
		info_append_string(db, "permission", perm_to_string(cmd->perm));
		// TODO - let this in when fully baked.
//		info_append_string(db, "usage", cmd->usage);
		cf_dyn_buf_chomp_char(db, ';');

		return;
	}

	// Commands expected via asinfo/telnet. If it's not in this list, it's a
	// "client-only" command, e.g. for cluster management.

	bool is_ce = as_error_enterprise_only();

	for (uint32_t i = 0; i < N_SPECS; i++) {
		const as_info_cmd* cmd = &SPECS[i];

		// Filter client only commands and EE only commands when build is CE.
		if (cmd->client_only || (is_ce && cmd->ee_only)) {
			continue;
		}

		cf_dyn_buf_append_string(db, cmd->name);
		cf_dyn_buf_append_char(db, ';');
	}

	cf_dyn_buf_chomp_char(db, ';');
}

static void
cmd_histogram(const char* name, const char* params, cf_dyn_buf* db)
{
	char value_str[128];
	int  value_str_len = sizeof(value_str);

	if (as_info_parameter_get(params, "namespace", value_str,
			&value_str_len) != 0) {
		cf_info(AS_INFO, "histogram %s command: no namespace specified", name);
		cf_dyn_buf_append_string(db, "error-no-namespace");
		return;
	}

	as_namespace* ns = as_namespace_get_byname(value_str);

	if (ns == NULL) {
		cf_info(AS_INFO, "histogram %s command: unknown namespace: %s",
				name, value_str);
		cf_dyn_buf_append_string(db, "error-unknown-namespace");
		return;
	}

	value_str_len = sizeof(value_str);

	if (as_info_parameter_get(params, "type", value_str, &value_str_len) != 0) {
		cf_info(AS_INFO, "histogram %s command:", name);
		cf_dyn_buf_append_string(db, "error-no-histogram-specified");
		return;
	}

	// get optional set field
	char set_name_str[AS_SET_NAME_MAX_SIZE];
	int set_name_str_len = sizeof(set_name_str);
	set_name_str[0] = 0;

	if (as_info_parameter_get(params, "set", set_name_str,
			&set_name_str_len) == -2) {
		cf_warning(AS_INFO, "set name too long");
		cf_dyn_buf_append_string(db, "ERROR::bad-set-name");
		return;
	}

	as_namespace_get_hist_info(ns, set_name_str, value_str, db);
}

